
public class mechka {

	int riba;
	int sun;
	
	public mechka(int riba, int sun) {
		super();
		this.riba = riba;
		this.sun = sun;
	}

	public int getRiba() {
		return riba;
	}

	public void setRiba(int riba) {
		this.riba = riba;
	}

	public int getSun() {
		return sun;
	}

	public void setSun(int sun) {
		this.sun = sun;
	}
	
	
}
